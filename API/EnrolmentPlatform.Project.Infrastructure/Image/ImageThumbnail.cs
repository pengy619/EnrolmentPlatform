        #region 自定义裁剪输出文件流
        public static void CutForCustomStream(string url)
        {
            var urlType = url.Split('.');
            var type = urlType[urlType.Length - 1];
            if (type.Equals("jpg") || type.Equals("jpeg") || type.Equals("gif") || type.Equals("swf") || type.Equals("png"))
            {
                //设置一个默认的图片
                var path = System.Web.HttpContext.Current.Server.MapPath("/upload/20170215180756.png");
                System.Drawing.Image sourceImg = new Bitmap(path);
                System.IO.MemoryStream imgStream = new System.IO.MemoryStream();
                int cutWidth = sourceImg.Width;
                int cutHeight = sourceImg.Height;
                sourceImg.Save(imgStream, System.Drawing.Imaging.ImageFormat.Jpeg);
                var fileStr = url.Split('/');
                var fileName = fileStr[fileStr.Length - 1];
                if (fileName.Contains("_"))
                {
                    var fileInfo = fileName.Split('_');
                    fileName = fileInfo[0] + "." + type;
                    cutWidth = Convert.ToInt32(fileInfo[1].Split('.')[0].Split('x')[0]);
                    cutHeight = Convert.ToInt32(fileInfo[1].Split('.')[0].Split('x')[1]);
                }
                if (File.Exists(System.Web.HttpContext.Current.Server.MapPath("/upload/" + fileName)))
                {
                    path = System.Web.HttpContext.Current.Server.MapPath("/upload/" + fileName);
                    sourceImg = new Bitmap(path);
                    if (cutWidth > sourceImg.Width)
                    {
                        cutWidth = sourceImg.Width;
                    }
                    if (cutHeight > sourceImg.Height)
                    {
                        cutHeight = sourceImg.Height;
                    }
                    //模版的宽高比例
                    double templateRate = (double)cutWidth / cutHeight;
                    //原图片的宽高比例
                    double initRate = (double)sourceImg.Width / sourceImg.Height;

                    //以大小为剪切大小，像素格式为32位RGB创建一个位图对像
                    Bitmap cutImg = new Bitmap(cutWidth, cutHeight, PixelFormat.Format32bppRgb);
                    //绘制高质量的缩小图对象
                    System.Drawing.Graphics temple = System.Drawing.Graphics.FromImage(cutImg);
                    temple.SmoothingMode = System.Drawing.Drawing2D.SmoothingMode.HighQuality;
                    temple.InterpolationMode = System.Drawing.Drawing2D.InterpolationMode.High;
                    temple.Clear(Color.White);
                    //定义一个裁剪区域
                    Rectangle cutArea = new Rectangle(0, 0, cutWidth, cutHeight);
                    if (templateRate.Equals(initRate))
                    {
                        //等比缩放
                        temple.DrawImage(sourceImg, cutArea, new System.Drawing.Rectangle(0, 0, sourceImg.Width, sourceImg.Height), System.Drawing.GraphicsUnit.Pixel);
                    }
                    else
                    {
                        //定位
                        Rectangle fromR = new Rectangle(0, (sourceImg.Height - sourceImg.Width) / 2, sourceImg.Height, sourceImg.Width);
                        Rectangle toR = new Rectangle(0, 0, sourceImg.Width, sourceImg.Width);
                        //画图
                        temple.DrawImage(sourceImg, toR, fromR, System.Drawing.GraphicsUnit.Pixel);
                    }
                    imgStream.Dispose();
                    imgStream = new System.IO.MemoryStream();
                    cutImg.Save(imgStream, System.Drawing.Imaging.ImageFormat.Jpeg);
                    cutImg.Dispose();
                    temple.Dispose();
                }
                HttpContext.Current.Response.ContentType = "image/jpeg";
                HttpContext.Current.Response.BinaryWrite(imgStream.ToArray());
                HttpContext.Current.Response.End();
                sourceImg.Dispose();
            }

        }
